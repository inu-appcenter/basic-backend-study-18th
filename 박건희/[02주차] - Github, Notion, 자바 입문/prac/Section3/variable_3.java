package Section3;
// 변수 선언 및 초기화
public class variable_3 {
    static void main() {
        /*
        int a; // int는 정수형 변수 선언
        int b;

        int c, d, g, f;
        System.out.println(a); 초기화 하지 않으면 더미 데이터가 출력될 가능성이 존재하며, 컴파일 오류 발생 O
        */

        int A = 10; // 변수를 선언과 초기화를 같이
        System.out.println(A);

        int B = 1, C = 10; // 여러 변수를 선언과 초기화를 같이
        System.out.println(B);
        System.out.println(C);
    }
}
