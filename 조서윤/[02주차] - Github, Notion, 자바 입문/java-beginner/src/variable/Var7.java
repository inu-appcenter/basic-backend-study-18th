package variable;

public class Var7 {

    static void main() {
        int a = 100; //정수 리터럴
        double b = 10.5; //실수 리터럴
        boolean c = true; //불리언(boolean) true, false 입력 가능
        char d = 'A'; //문자 하나, 작은따옴표로 감싸야 함
        String e = "Hello Java"; //문자열, 문자열을 다루기 위한 특별한 타입, 앞 글자 대문자로 해야 함

        //int z = "백원";
        //int z = "100";

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
