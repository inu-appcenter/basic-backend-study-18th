package Operator;

//3강_7
public class HelloJava3_7 {
    public static void main(String[] args){
        //대입 연산자(=) : 값을 변수에 할당하는 연산자
        // 축약 대입 연산자(+=, -=,*=, /=, %=)
        int a=5;
        a+=3;
        System.out.println(a); //8
        a-=2;
        System.out.println(a); //6
        a*=4;
        System.out.println(a); //24
        a/=3;
        System.out.println(a); //8
        a%=5;
        System.out.println(a); //3


    }
}
