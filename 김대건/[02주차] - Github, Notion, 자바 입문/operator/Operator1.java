package operator;

public class Operator1 {

    public static void main(String[] args) {
        int a = 5, b = 4;

        int sum = a+b;
        System.out.println("a + b = "+ sum);
        int diff = a-b;
        System.out.println("a - b = "+  diff);
        int muli = a*b;
        System.out.println("a * b = "+  muli);
        int div = a/b;
        System.out.println("a / b = "+ div);
        int mod = a%b;
        System.out.println("a % b = "+ mod);

    }
}
