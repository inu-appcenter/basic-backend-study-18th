package Section3;
// 변수 문제와 풀이
public class variable_5 {
    static void main() {

        // Q1.
        int num1 = 10, num2 = 5;

        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);

        // Q2.
        int num3 = 10, num4 = 20;
        int sum = num3 + num4;
        System.out.println(sum);

        // Q3.
        long longVar = 10000000000L;
        System.out.println(longVar);

        boolean booleanVar = true;
        System.out.println(booleanVar);

    }
}
