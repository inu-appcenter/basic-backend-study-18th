package Section4;
//대입 연산자
public class operator_7 {
    static void main() {

        int a = 5; // 5

        //변수의 값을 변환 산술 연산자에 해당하는 연산 후 저장
        a += 3;
        System.out.println(a);
        a -= 2;
        System.out.println(a);
        a *= 4;
        System.out.println(a);
        a /= 3;
        System.out.println(a);
        a %= 5;
        System.out.println(a);



    }
}
